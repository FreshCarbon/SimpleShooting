using UnityEngine;

// 플레이어 캐릭터를 조작하기 위한 사용자 입력을 감지
// 감지된 입력값을 다른 컴포넌트들이 사용할 수 있도록 제공
public class PlayerInput : MonoBehaviour
{
    //public string rotateAxisName = "Horizontal"; // 좌우 회전을 위한 입력축 이름

    public string moveAxisName = "Vertical"; // 움직임을 위한 입력축 이름
    public string moveHorizontalName = "Horizontal";
    public string jumpKeyName = "Jump";

    public string fireButtonName = "Fire1"; // 발사를 위한 입력 버튼 이름
    public string reloadButtonName = "Reload"; // 재장전을 위한 입력 버튼 이름

    public Gun gun; // 사용할 총    

    // 값 할당은 내부에서만 가능
    public float moveVertical { get; private set; } // 감지된 움직임 입력값
    public float moveHorizontal { get; private set; }
    public bool jump { get; private set; }

    public float rotate { get; private set; } // 감지된 회전 입력값
    public bool fire { get; private set; } // 감지된 발사 입력값
    public bool reload { get; private set; } // 감지된 재장전 입력값

    // 매프레임 사용자 입력을 감지
    private void Update()
    {
        // 게임오버 상태에서는 사용자 입력을 감지하지 않는다  
        if (GameManager.instance != null && GameManager.instance.isGameover)
        {
            moveVertical = 0;
            moveHorizontal = 0;
            rotate = 0;
            fire = false;
            reload = false;
            jump = false;
            return;
        }

        // move에 관한 입력 감지
        moveVertical = Input.GetAxis(moveAxisName);
        moveHorizontal = Input.GetAxis(moveHorizontalName);
        jump = Input.GetKeyDown("space");

        if(jump)
        {
            Debug.Log("JumpKey Pressed");
        }
        // fire 입력 감지
        fire = Input.GetButton(fireButtonName);

        // reload 입력 감지
        reload = Input.GetButtonDown(reloadButtonName);
    }
}